<div class="row" style="padding:20px 0;">
	<div class="col-md-12">
		<p>No matter the protective measures taken, it is a simple market fact that borrowers default on loan
terms or payments. In some cases, the overall amount may be too much for the debtor to manage,
and continuing payments may force it into bankruptcy. A creditor can decide that rather than gamble on
a debtor’s future liquidity, an immediate debt settlement agreement will make the best of a declining
situation. The borrower can get part of its burden lifted, eliminating continuing payments and growing
default and interest costs, without sacrificing its credit score or business relationships</p>
		<p>The enclosed document allows the lender to forgive part of what a debtor owes if it receives an
immediate settlement amount, and contains everything you’ll need to release both parties from their
obligations. A written contract minimizes confusion, misunderstanding, and error, and sets forth the
parties’ expectations and fulfillment obligations. In every way, this promotes successful and profitable
business arrangements. </p>
		<ul>
			<li>Debt settlement is a means of reducing or eliminating unsecured debt by negotiating an agreed
upon payoff amount with creditors. This usually does not occur if a debt is secured, since the lender
will have the right to take the property that secures the loan in lieu of payment</li>
			<li>Settling a debt can result in income tax liability. Creditors must report any forgiven debt in excess of
$600 to the IRS, and the debtor will receive an IRS form for the amount of the forgiven debt. Talk to
an attorney or a tax professional for additional details about these consequences.</li>
			<li>There are pros and cons for the borrower looking to settle a debt. Although your monthly payments
will be reduced, you will usually need to make an immediate large sum payment to complete the
settlement. Your creditors may report any settlement to the credit bureaus. If you have a good
credit score, this will have an immediate and large negative impact. If your credit score is bad, debt
settlement or negotiation may have less of an impact and may be a better choice. Consider these
and other personal factors before entering into a binding settlement agreement. </li>
<li>There are also pros and cons for the lender looking to settle the debt. It can recover money that
would be unavailable if the debtor entered bankruptcy: even a partial settlement is better than
nothing. However, the creditor is ending the possibility of obtaining the total amount that it is owed.</li>
<li>Before sitting down to write, decide exactly what your goals are for the settlement. How much of
the debt balance will be paid off? When will this payment need to be made? Clarify these terms
before writing them down.</li>
<li>Allow each party to spend time reviewing the agreement. This will reduce the likelihood, or at least
the efficacy, of a claim that a party did not understand any terms or how those might affect the
agreement. </li>
<li>Both parties should review the agreement carefully to ensure that all relevant deal points have
been included. It is better to be over-inclusive than under-inclusive. Do not assume that certain
expectations or terms are agreed to if they are not stated expressly on the document.</li>
<li>Sign two copies of the agreement, one for you and one for the other party.</li>
<li>Depending on the nature of its terms, you may decide to have the contract witnessed or notarized.
This will limit later challenges to the validity of a party’s signature. </li>
<li>If your agreement is complicated, do not use the enclosed form. Contact an attorney to help you
draft a document that will meet your specific needs. </li>
		</ul>
